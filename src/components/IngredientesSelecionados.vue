<script lang="ts">
import Tag from "./Tag.vue";

export default {
    components: { Tag },
    props: {
        ingrediente: { type: String, required: true }
    },
    data() {
        return {
            selecionado: false
        }
    },
    methods: {
        aoClicar() {
            this.selecionado = !this.selecionado

            if(this.selecionado) {
                this.$emit('adicionarIngrediente',this.ingrediente); // com essa function estou criando um evento personalizado
            } else {
                this.$emit('removerIngrediente', this.ingrediente);
            }
        }
    },
    emits: ['adicionarIngrediente', 'removerIngrediente'] //boa pr√°tica
}
</script>

<template>
    <button
     class="ingrediente" 
     @click="aoClicar"
    :aria-pressed="selecionado"
    >
        <Tag :texto="ingrediente" :ativa="selecionado" />
    </button>
</template>

<style scoped>

.ingrediente {
    cursor: pointer
}

</style>